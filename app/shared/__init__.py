"""
Общие файлы для проекта.

Содержит базовые типы данных, конфигурацию и вспомогательные функции,
которые используются во всех слоях приложения.

Состав пакета:
    - **config.py**: Загрузка переменных окружения (.env), определение путей к файлам
      и хранение константных настроек бирж (комиссии, лимиты).

    - **decorators.py**: Декоратор для стандартизации запуска скриптов,
      отвечающий за настройку логирования, обработку исключений и совместимость с Windows (asyncio).

    - **primitives.py**: Базовые Enums и Dataclasses для строгой типизации
      (направление торговли, статус позиции, профиль риска).

    - **events.py**: Классы событий для шины данных (Event Bus). Описывает структуру
      сообщений: рыночные данные, сигналы стратегий, ордера и отчеты об исполнении.

    - **schemas.py**: Pydantic-модели для валидации входящих данных (параметры стратегий,
      конфигурация запуска, данные из API).

    - **logging_setup.py**: Инициализация логгеров. Настройка форматов вывода
      в консоль и файл, фильтрация логов для разных режимов (Backtest/Live).

    - **time_helper.py**: Функции для работы со временем: парсинг строковых интервалов
      в timedelta и управление часовыми поясами.
"""