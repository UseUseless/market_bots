"""
Общие файлы для всего проекта.

Содержит базовые типы данных, конфигурацию и вспомогательные функции,
которые могут использоваться в любой части приложения.

Состав пакета:
    - **config.py**: Загрузка переменных окружения (.env), определение путей к файлам
      и хранение настроек бирж (комиссии).

    - **decorators.py**: Декоратор для стандартизации запуска скриптов,
      отвечающий за настройку логирования, обработку исключений и совместимость с Windows (в части asyncio).

    - **types.py**: Базовые Enums и Dataclasses для строгой типизации
      (направление торговли, статус позиции, профиль риска).

    - **events.py**: Классы событий для очереди событий. Описывает события: 
    рыночные данные, сигналы стратегий, ордера и отчеты об исполнении.

    - **interfaces.py**: "Порты" (контракты). Абстрактные классы, определяющие,
      как ядро взаимодействует с внешним миром или внутри проекта.

    - **schemas.py**: Pydantic-модели для валидации входящих данных (для стратегий).

    - **logging_setup.py**: Инициализация логгеров. Настройка форматов вывода
      в консоль и файл, фильтрация логов для разных режимов (Backtest/Live).

    - **time_utils.py**: Функции для работы со временем: конвертация интервалов
      в timedelta и управление часовыми поясами.

    - **factories.py**: Фабрики для сборки объемных DTO и т.п.
"""