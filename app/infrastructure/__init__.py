"""
Инфраструктурный слой (Infrastructure Layer).

В терминологии Гексагональной архитектуры (Ports & Adapters), этот пакет
содержит **Адаптеры** (Adapters), которые реализуют интерфейсы (Ports),
определенные в ядре (`app.core.interfaces`).

Роль слоя:
    Изолировать бизнес-логику от деталей реализации внешних сервисов.
    Ядро приложения (Core) не знает, какая именно используется база данных,
    биржа или файловая система. Оно работает с абстракциями, а этот слой
    предоставляет конкретные реализации этих абстракций.

Состав пакета:
    - **database**: Работа с реляционной базой данных (SQLite) через SQLAlchemy.
      Хранение конфигураций, состояния ботов и портфелей.

    - **exchanges**: Клиенты для API бирж (Bybit, Tinkoff).
      Аутентификация, соблюдение лимитов, унификация форматов данных.

    - **execution**: Реализация отправки ордеров и получения подтверждений (Fills)
      в реальном времени.

    - **feeds**: Поставщики рыночных данных. Адаптеры для WebSocket/gRPC стримов
      и чтения локальных файлов.

    - **storage**: Работа с файловой системой. Загрузка исторических данных,
      сохранение логов сделок и метаданных.
"""