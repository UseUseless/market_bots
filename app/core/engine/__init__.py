"""
Пакет движков исполнения.

Этот пакет содержит реализаций для различных режимов работы системы.
Каждый движок адаптирован под специфические требования своего режима (скорость,
асинхронность или параллелизм).

1.  **backtest** (Синхронный / Event-Driven):
    -   Симулятор торговли на исторических данных.
    -   Реализует событийную модель (Event Loop), обрабатывая историю свеча за свечой.
    -   В режиме пакетного тестирования (Batch) использует `ThreadPoolExecutor`
        для одновременного прогона множества инструментов.

2.  **live** (Асинхронный / IO-Bound):
    -   Движок реального времени для мониторинга сигналов.
    -   Построен на `asyncio` для эффективного управления сотнями сетевых соединений
        (WebSocket стримы бирж, Telegram боты) в одном потоке.
    -   Поддерживает "горячую" перезагрузку (Hot Reload) конфигураций из БД без остановки.

3.  **optimization** (Итеративный / WFO):
    -   Реализует алгоритм Walk-Forward Optimization (Скользящее окно).
    -   Использует библиотеку `Optuna` для байесовского поиска гиперпараметров.
    -   Применяет `concurrent.futures.ThreadPoolExecutor` для:
        - Параллельной загрузки данных (Data Slicing).
        - Параллельного расчета портфелей внутри целевой функции (Objective).
"""