"""
Ядро бизнес-логики (Core).

Этот модуль содержит всю "интеллектуальную" часть приложения: алгоритмы торговли,
управление состоянием портфеля, расчет рисков и движки для запуска стратегий.
Код здесь максимально абстрагирован от внешней инфраструктуры (бирж, БД, UI).

Состав пакета:
    - **interfaces.py**: "Порты" (контракты). Абстрактные классы, определяющие,
      как ядро взаимодействует с внешним миром (DataFeed, ExecutionHandler, Repository).

    - **event_bus.py**: Шина событий. Механизм, связывающий разрозненные компоненты
      (Стратегию, Портфель, UI) в единую систему без жестких связей.

    - **calculations**: Математика. Расчет технических индикаторов и фичей для ML.

    - **engine**: "Движки" системы.
        - `backtest`: Симуляция торговли на исторических данных.
        - `live`: Оркестратор для реальной торговли/мониторинга.
        - `optimization`: Подбор параметров (Walk-Forward Optimization).

    - **portfolio**: Учет финансов. Хранение состояния (баланс, позиции) и
      расчет PnL (Accounting).

    - **risk**: Риск-менеджмент. Логика ограничения убытков и расчета размера позиции.

    - **execution**: Логика исполнения. Превращение сигналов в ордера и симуляция
      биржевого исполнения (проскальзывание).

    - **analysis**: Аналитика. Расчет метрик эффективности (Sharpe, Drawdown)
      и генерация отчетов.
"""