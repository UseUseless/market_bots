"""
–°–∫—Ä–∏–ø—Ç –∑–∞–ø—É—Å–∫–∞ –≤–µ–±-–∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞ (Dashboard).

–≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç –≤—ã—Å—Ç—É–ø–∞–µ—Ç –æ–±–µ—Ä—Ç–∫–æ–π –¥–ª—è –∑–∞–ø—É—Å–∫–∞ Streamlit –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è.
–û–Ω –æ–ø—Ä–µ–¥–µ–ª—è–µ—Ç –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ –ø—É—Ç–∏ –∫ —Ç–æ—á–∫–µ –≤—Ö–æ–¥–∞ –¥–∞—à–±–æ—Ä–¥–∞ –∏ –∑–∞–ø—É—Å–∫–∞–µ—Ç –µ–≥–æ
–∫–∞–∫ –æ—Ç–¥–µ–ª—å–Ω—ã–π –ø–æ–¥–ø—Ä–æ—Ü–µ—Å—Å, –∏—Å–ø–æ–ª—å–∑—É—è —Ç–µ–∫—É—â–µ–µ –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ Python.

–î–∞—à–±–æ—Ä–¥ –ø–æ–∑–≤–æ–ª—è–µ—Ç –≤–∏–∑—É–∞–ª–∏–∑–∏—Ä–æ–≤–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –±—ç–∫—Ç–µ—Å—Ç–æ–≤, —Å—Ä–∞–≤–Ω–∏–≤–∞—Ç—å —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏
–∏ —É–ø—Ä–∞–≤–ª—è—Ç—å –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–µ–π –±–æ—Ç–æ–≤.

–ó–∞–ø—É—Å–∫:
    python scripts/run_dashboard.py
"""

import os
import subprocess
import sys

# –î–æ–±–∞–≤–ª—è–µ–º –∫–æ—Ä–Ω–µ–≤—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø—Ä–æ–µ–∫—Ç–∞ –≤ sys.path –ü–ï–†–ï–î –∏–º–ø–æ—Ä—Ç–æ–º app
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from app.shared.config import config

BASE_DIR = config.BASE_DIR


def main() -> None:
    """
    –û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∑–∞–ø—É—Å–∫–∞.

    –ê–ª–≥–æ—Ä–∏—Ç–º —Ä–∞–±–æ—Ç—ã:
    1. –í—ã—á–∏—Å–ª—è–µ—Ç –∞–±—Å–æ–ª—é—Ç–Ω—ã–π –ø—É—Ç—å –∫ —Ñ–∞–π–ª—É `main.py` –≤–Ω—É—Ç—Ä–∏ –º–æ–¥—É–ª—è dashboard.
    2. –ü—Ä–æ–≤–µ—Ä—è–µ—Ç —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ —ç—Ç–æ–≥–æ —Ñ–∞–π–ª–∞.
    3. –ó–∞–ø—É—Å–∫–∞–µ—Ç –∫–æ–º–∞–Ω–¥—É `streamlit run ...` —á–µ—Ä–µ–∑ `subprocess`.
       –ò—Å–ø–æ–ª—å–∑—É–µ—Ç `sys.executable`, —á—Ç–æ–±—ã –≥–∞—Ä–∞–Ω—Ç–∏—Ä–æ–≤–∞—Ç—å –∑–∞–ø—É—Å–∫ –∏–º–µ–Ω–Ω–æ –≤ —Ç–æ–º
       Python –æ–∫—Ä—É–∂–µ–Ω–∏–∏ (venv), –≤ –∫–æ—Ç–æ—Ä–æ–º –∑–∞–ø—É—â–µ–Ω —ç—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç.
    """
    dashboard_path = os.path.join(BASE_DIR, "app", "adapters", "dashboard", "main.py")

    print(f"üöÄ –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –¥–∞—à–±–æ—Ä–¥–∞...")
    print(f"üìÇ –ü—É—Ç—å –∫ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—é: {dashboard_path}")

    if not os.path.exists(dashboard_path):
        print(f"‚ùå –ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê: –§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω: {dashboard_path}")
        return

    print("üåê –ó–∞–ø—É—Å–∫ Streamlit —Å–µ—Ä–≤–µ—Ä–∞... (–ù–∞–∂–º–∏—Ç–µ Ctrl+C –¥–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏)")

    # Streamlit –∑–∞–ø—É—Å–∫–∞–µ—Ç—Å—è –∫–∞–∫ –æ—Ç–¥–µ–ª—å–Ω—ã–π –ø—Ä–æ—Ü–µ—Å—Å
    try:
        # cwd=BASE_DIR –≤–∞–∂–µ–Ω, —á—Ç–æ–±—ã Streamlit –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ –≤–∏–¥–µ–ª –ø–∞–ø–∫—É .streamlit –∏ –∫–æ—Ä–µ–Ω—å –ø—Ä–æ–µ–∫—Ç–∞
        subprocess.run(
            [sys.executable, "-m", "streamlit", "run", dashboard_path],
            cwd=BASE_DIR,
            check=True
        )
    except KeyboardInterrupt:
        print("\nüõë –î–∞—à–±–æ—Ä–¥ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º.")
    except subprocess.CalledProcessError as e:
        print(f"\n‚ùå –î–∞—à–±–æ—Ä–¥ —É–ø–∞–ª —Å –æ—à–∏–±–∫–æ–π (–∫–æ–¥ {e.returncode}).")
    except Exception as e:
        print(f"\n‚ùå –ù–µ–ø—Ä–µ–¥–≤–∏–¥–µ–Ω–Ω–∞—è –æ—à–∏–±–∫–∞ –∑–∞–ø—É—Å–∫–∞: {e}")


if __name__ == "__main__":
    main()